"""Publish docs/fixtures.js for the static Match Centre.

    python scripts/fetch_fixtures.py   # optional — refresh from API
    python scripts/publish_fixtures.py
"""
from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

import sys

sys.path.insert(0, str(Path(__file__).resolve().parent))
from wc26_groups import GROUPS

PROJECT_ROOT = Path(__file__).resolve().parent.parent
API_PATH = PROJECT_ROOT / "data" / "processed" / "wc26_fixtures.json"
OUTPUT_PATH = PROJECT_ROOT / "docs" / "fixtures.js"


def synthetic_fixtures() -> list[dict]:
    """Fallback round-robin when the API has no WC events yet."""
    fixtures = []
    for group, teams in GROUPS.items():
        pairs = [
            (teams[0], teams[1], 1),
            (teams[2], teams[3], 1),
            (teams[0], teams[2], 2),
            (teams[3], teams[1], 2),
            (teams[3], teams[0], 3),
            (teams[1], teams[2], 3),
        ]
        for home, away, md in pairs:
            fixtures.append(
                {
                    "id": f"synthetic-{group}-{home}-{away}",
                    "home": home,
                    "away": away,
                    "group": group,
                    "md": md,
                    "kickoff": "",
                    "status": "pending",
                    "homeScore": None,
                    "awayScore": None,
                    "bettable": False,
                    "league": "World Cup (model schedule — dates TBD)",
                }
            )
    return fixtures


def main() -> None:
    source = "synthetic"
    updated_at = datetime.now(timezone.utc).isoformat()
    fixtures = synthetic_fixtures()

    if API_PATH.exists():
        data = json.loads(API_PATH.read_text(encoding="utf-8"))
        api_fixtures = data.get("fixtures") or []
        if api_fixtures:
            fixtures = api_fixtures
            source = data.get("source", "api")
            updated_at = data.get("updatedAt", updated_at)

    payload = (
        "// Auto-generated by scripts/publish_fixtures.py — do not edit by hand.\n"
        f"const FIXTURES_META = {json.dumps({'source': source, 'updatedAt': updated_at}, indent=2)};\n"
        f"const WC26_FIXTURES = {json.dumps(fixtures, indent=2, ensure_ascii=False)};\n"
    )
    OUTPUT_PATH.write_text(payload, encoding="utf-8")
    print(f"Wrote {OUTPUT_PATH} ({len(fixtures)} fixtures, source={source})")


if __name__ == "__main__":
    main()
