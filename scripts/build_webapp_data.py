"""Generate docs/data.js from the processed feature matrix.

Run after rebuilding features so the static web app stays in sync:

    python scripts/build_webapp_data.py
"""
import csv
import json
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
FEATURES_PATH = PROJECT_ROOT / "data" / "processed" / "blended_model_features.csv"
OUTPUT_PATH = PROJECT_ROOT / "docs" / "data.js"

# keep in sync with src/models/poisson_engine.py — see comment there for calibration
GLOBAL_BASE_XG = 2.571


def main():
    with open(FEATURES_PATH, newline="", encoding="utf-8") as fh:
        rows = list(csv.DictReader(fh))
    teams = {
        row["team"]: {
            "att": round(float(row["attack_strength"]), 6),
            "def": round(float(row["defense_weakness"]), 6),
            "squad": round(float(row["squad_rating"]), 3),
        }
        for row in rows
    }
    payload = (
        "// Auto-generated by scripts/build_webapp_data.py — do not edit by hand.\n"
        f"const GLOBAL_BASE_XG = {GLOBAL_BASE_XG};\n"
        f"const TEAM_RATINGS = {json.dumps(teams, indent=2, ensure_ascii=False)};\n"
    )
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(payload, encoding="utf-8")
    print(f"Wrote {OUTPUT_PATH} with {len(teams)} teams.")


if __name__ == "__main__":
    main()
